fun main() {
    val a = true
    val b = false
    val c = false

    // a) A или B
    val resultA = a || b
    println("A или B: $resultA")

    // b) A и B
    val resultB = a && b
    println("A и B: $resultB")

    // c) B или C
    val resultC = b || c
    println("B или C: $resultC")
}
